<font class="page-title">Zasada działania rozgrywek</font>
<ol>
    <li>Zgłaszasz się do nauczyciela wf.</li>
    <li>Otrzymujesz konto na stronie.</li>
    <li>Wyzywasz innego gracza z listy Rankingu poprzez wciśnięcie przycisku "<font style="font-weight: bold">Wyzwij</font>".</li>
    <li>Rozgrywasz mecz.</li>
    <li>Po zakończonym meczu, obaj na swoim profilu wpisujecie wynik meczu, w zakładce <font style="font-weight: bold">MOI PRZECIWNICY</font>.</li>
    <li>Jeżeli wyniki są prawidłowe po obu stronach, mecz zapisuje się jako zakończony, a punkty zostają dodane do waszego konta.</li>
    <li>Jeżeli wyniki wpisane przez was są sprzeczne, idą do weryfikacji do administratora (nauczyciela WF-u). W tej sytuacji nauczyciel rozstrzyga sprzeczność wyniku.</li>
</ol>

<font class="page-title">Zasada działania serwisu</font>
<ol>
    <li>W zakłdace <font style="font-weight: bold">RANKING</font> znajduje się aktualny ranking graczy, z którego można wyzywać na pojedynek kolejnych graczy.</li>
    <li>W zakładce <font style="font-weight: bold">MOI PRZECIWNICY</font> znajdować się będą wyzwania rzucone przez nas oraz rzucone nam, które czekają na akceptację przez drugiego gracza. Po zaakceptowaniu meczu, pokazuje się tabelka z miejscem na wpisanie wyniku, po zakończonym meczu.</li>
    <li>W zakładce <font style="font-weight: bold">MOJE KONTO</font> znajduje się historia rozegranych meczy.</li>
    <li>Zmiana hasła, E-m@ilu, oraz numeru telefonu jest możliwa tylko przez administratorów (nauczycieli WF-u).</li>
</ol>